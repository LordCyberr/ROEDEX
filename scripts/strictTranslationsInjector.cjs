for (const lang of Object.keys(fullSettingsTranslations)) {
  const data = fullSettingsTranslations[lang];
  // More resilient regex replacing just the 'settings' block. We know what 'translations.ts' looks like.
  // Actually, we can use exact match if we can predict the end, but let's replace everything after 'tabs' up to 'columns'
  const settingsRegex = new RegExp(`(${lang}: \\{[\\s\\S]*?tabs: \\{[\\s\\S]*?\\},)\\s*settings: \\{[\\s\\S]*?\\},\\s*(columns: \\{)`);
  content = content.replace(settingsRegex, (match, p1, p2) => {
    let toInject = `\n    settings: ${JSON.stringify(data.settings, null, 6)},\n    changelog: ${JSON.stringify(data.changelog, null, 6)},\n    tutorial: ${JSON.stringify(data.tutorial, null, 6)},\n    companions: ${JSON.stringify(data.companions, null, 6)},\n    `;
    toInject = toInject.replace(/\"([a-zA-Z0-9_]+)\":/g, "$1:");
    return p1 + toInject + p2;
  });
}

fs.writeFileSync('src/i18n/translations.ts', content, 'utf8');
