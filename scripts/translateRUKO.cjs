const lines = content.split('\\n');

function replaceLines(targetStartLines, replacementStr) {
  let inBlock = false;
  let blockStart = -1;
  let blockEnd = -1;
  
  // Find the exact line
  for (let i = 0; i < lines.length; i++) {
    if (lines[i] === targetStartLines[0] && lines[i+1] === targetStartLines[1] && lines[i+2] === targetStartLines[2]) {
       blockStart = i;
       for (let j = i; j < lines.length; j++) {
         if (lines[j].trim() === 'zoneGuild: [') {
           blockEnd = j - 1; // line before zoneGuild
           break;
         }
       }
       break;
    }
  }
  
  if (blockStart !== -1 && blockEnd !== -1) {
    const replLines = replacementStr.split('\\n');
    lines.splice(blockStart, blockEnd - blockStart + 1, ...replLines);
  }
}

